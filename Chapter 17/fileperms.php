<?php ##Використання функції fileperms()
//Отримуємо права доступу та тип файлу
$perms = fileperms('text.txt');
//Перетворюємо результат у вісьмеричну систему числення
echo decoct($perms).'<br>'; //100666
//Перетворюємо результат у двоїчну систему числення
echo decbin($perms).'<br>'; //1000000110110110