<?php
//Передача параметрів за посиланням
function increment(&$a) {
    echo 'Значення $a до інкременту: '.$a.'<br>';
    $a++;
    echo 'Значення $a після інкрименту: '.$a.'<br>';
}

$num = 10;
echo 'Значення $num до виконання функції: '.$num.'<br>';
increment($num);
echo 'Значення $num після виконання функції: '.$num.'<br>';